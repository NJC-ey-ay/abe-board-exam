function qToTS(q) {
  const esc = s => s.replace(/'/g, "\\'");
  const opts = q.options.map(o => `'${esc(o)}'`).join(', ');
  const steps = q.solution.steps.map(s => `'${esc(s)}'`).join(', ');
  const weak = q.weakPoints.map(w => `'${esc(w)}'`).join(', ');
  const rf = q.relatedFormulas ? `\n    relatedFormulas: [${q.relatedFormulas.map(f => `'${f}'`).join(', ')}],` : '';
  return `  {
    id: '${q.id}',
    area: '${q.area}',
    subTopic: '${q.subTopic}',
    topic: '${q.topic}',
    difficulty: '${q.difficulty}',
    question: '${esc(q.question)}',
    options: [${opts}],
    correctAnswer: ${q.correctAnswer},
    solution: {
      keyConcept: '${esc(q.solution.keyConcept)}',
      steps: [${steps}]
    },${rf}
    weakPoints: [${weak}]
  }`;
}

